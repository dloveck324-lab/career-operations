/**
 * Backfill `evaluations.green_flags_json` / `red_flags_json` for legacy rows
 * by parsing the Pros/Cons bullet sections out of `jobs.score_reason`.
 *
 * After the per-line-feedback migration shipped, existing evals had the new
 * columns NULL — which made the drawer render the legacy text blob with no
 * thumbs. This script lights up the thumbs for historical jobs without
 * spending tokens on a re-eval.
 *
 * Usage:
 *   node scripts/backfill-eval-flags.mjs --dry-run   # preview
 *   node scripts/backfill-eval-flags.mjs             # apply
 */
import Database from 'better-sqlite3'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const DB_PATH = resolve(__dirname, '../data/jobs.db')
const DRY_RUN = process.argv.includes('--dry-run')

/**
 * Parse a `score_reason` blob into {green, red} arrays. The blob is
 * generated by `parseEvalResponse()` in this format:
 *
 *   <dim line>
 *   <verdict prose>
 *   Pros
 *   • flag1
 *   • flag2
 *   Cons
 *   • flag1
 *   • flag2
 *
 * Returns empty arrays if the section isn't found.
 */
function parseFlags(scoreReason) {
  if (!scoreReason || typeof scoreReason !== 'string') return { green: [], red: [] }
  const extractSection = (label) => {
    const re = new RegExp(`(?:^|\\n)${label}\\n([\\s\\S]+?)(?=\\n\\n[A-Z]|$)`)
    const m = scoreReason.match(re)
    if (!m) return []
    return m[1]
      .split('\n')
      .map((l) => l.replace(/^\s*[•\-*]\s*/, '').trim())
      .filter(Boolean)
  }
  return { green: extractSection('Pros'), red: extractSection('Cons') }
}

const db = new Database(DB_PATH)

// Fetch the latest evaluation per job alongside its job.score_reason.
const rows = db.prepare(`
  SELECT e.id AS eval_id, e.job_id, e.green_flags_json, e.red_flags_json, j.score_reason
  FROM evaluations e
  INNER JOIN jobs j ON j.id = e.job_id
  WHERE (e.green_flags_json IS NULL OR e.red_flags_json IS NULL)
    AND j.score_reason IS NOT NULL
    AND length(j.score_reason) > 0
`).all()

console.log(`Found ${rows.length} eval row(s) missing flags with a parseable score_reason.\n`)

const update = db.prepare(`
  UPDATE evaluations
  SET green_flags_json = COALESCE(green_flags_json, @green_json),
      red_flags_json   = COALESCE(red_flags_json, @red_json)
  WHERE id = @id
`)

let updated = 0
let parsed = 0
let skipped = 0
const samples = []

const tx = db.transaction(() => {
  for (const r of rows) {
    const { green, red } = parseFlags(r.score_reason)
    if (green.length === 0 && red.length === 0) { skipped++; continue }
    parsed++
    if (samples.length < 3) {
      samples.push({ eval_id: r.eval_id, job_id: r.job_id, green: green.slice(0, 2), red: red.slice(0, 2) })
    }
    if (!DRY_RUN) {
      update.run({
        id: r.eval_id,
        green_json: green.length ? JSON.stringify(green) : null,
        red_json: red.length ? JSON.stringify(red) : null,
      })
      updated++
    }
  }
})
tx()

console.log(`Parsed bullets for ${parsed} eval(s).`)
console.log(`Skipped ${skipped} (no Pros/Cons bullets found in score_reason).`)
console.log(`${DRY_RUN ? 'Would update' : 'Updated'} ${DRY_RUN ? parsed : updated} row(s).\n`)

if (samples.length > 0) {
  console.log('Sample parses:')
  for (const s of samples) {
    console.log(`  eval=${s.eval_id} job=${s.job_id}`)
    if (s.green.length) console.log(`    green: ${s.green.map((g) => JSON.stringify(g)).join(', ')}`)
    if (s.red.length)   console.log(`    red:   ${s.red.map((r) => JSON.stringify(r)).join(', ')}`)
  }
}

if (DRY_RUN) console.log('\n(dry run — no DB changes)')
